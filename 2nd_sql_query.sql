create database Muhsina;
use Muhsina ;
create table department(id varchar(10) primary key ,name varchar(25), mgreno int);
insert into department (id,name,mgreno) values ('D1','Consulting',1001),('D2','Development',1002),('D3','Research',1003);
create table employee(ENO int  primary key ,ENAME varchar(25), BDATE date,TITLE varchar(25),salary int,DNO varchar(10),foreign key(DNO)references department(id));
insert into employee (ENO,ENAME,BDATE,TITLE,salary,DNO) values(1001,'Alice','1980-03-10','Manager',85000,'D1'),(1002,'Bob','1985-07-14','EE',40000,'D2'),(1003,'Charlie','1988-09-12','SA',37000,'D1'),(1004,'David','1985-07-14','EE',32000,'D3'),(1005,'Eve','1992-11-05','Manager',90000,'D2');
create table project(PNO int primary key,PNAME varchar(25),BUDGET int,DNO varchar(10),foreign key(DNO)references department(id));
insert into project(PNO,PNAME,BUDGET,DNO) values(501,'Alpha',120000,'D1'),(502,'Beta',45000,'D2'),(503,'Gamma',75000,'D3'),(504,'Delta',200000,'D1');
create table workson (ENO int primary key,PNO int,foreign key(PNO)references project(PNO),RESP varchar(25),HOURS int);
insert into workson(ENO,PNO,RESP,HOURS) values(1001,501,'manager',15),(1002,502,'Developer',8),(1003,501,'Consultant',12),(1004,503,'Tester',9),(1005,504,'manager',18);
select * from department;
select * from employee;
select * from project;
select * from workson;
select PNO,PNAME from project where BUDGET > 100000;
select * from workson where HOURS < 10 and RESP = 'manager';
select ENO,ENAME from employee where (TITLE = 'EE' or TITLE = 'SA') and salary > 35000;
select ENAME from employee where DNO = 'D1' order by salary desc;
select * from department order by name asc;
select e.ENAME,d.name as DNAME,e.TITLE from employee e join department d on e.DNO = d.id;
select p.PNAME,w.HOURS,w.PNO from workson w join project p on w.PNO = p.PNO where w.HOURS > 10;
select p.PNAME,d.name as DNAME,p.BUDGET from project p join department d on p.DNO = d.id where p.BUDGET < 50000;
select e.ENO,e.salary from employee e join department d on e.DNO = d.id where d.name = 'Consulting' order by e.salary desc;
select e.ENAME,p.PNAME,e.TITLE,w.HOURS from workson w join project p on w.PNO = p.PNO join employee e on w.ENO = e.ENO;